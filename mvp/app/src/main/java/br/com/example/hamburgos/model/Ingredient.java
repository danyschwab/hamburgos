package br.com.example.hamburgos.model;

public class Ingredient {

    private int id;
    private String name;
    private float price;
    private String image;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public float getPrice() {
        return price;
    }

    public String getImage() {
        return image;
    }
}
